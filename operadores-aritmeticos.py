#Operadores aritméticos
'''
    PRIORIDAD DE OPERADORES
    1. PARÉNTESIS ()
    2. EXPONENCIACIÓN ** - POTENCIAS
    3. MULTIPLICACIÓN *
    4. DIVISIÓN /
    5. ADICIÓN +
    6. SUSTRACCIÓN -
'''

numero1 = 10
numero2 = 5
resultadoParentesis = (numero1 - numero2)+(numero1 + numero2)
resultadoPotencia = numero1 ** numero2
resultadoMultiplicacion = numero1 * numero2
resultadoDivision = numero1 / numero2
resultadoModulo = numero1 % numero2
resultadoAdicion = numero1 + numero2
resultadoSustraccion = numero1 - numero2
print(f"Número 1: {numero1}")
print(f"Número 2: {numero2}")
print("----------------------------")
print(f"Resultado Paréntesis: {resultadoParentesis}")
print("----------------------------")
print(f"Resultado Potencia: {resultadoPotencia}")
print("----------------------------")
print(f"Resultado Multiplicación: {resultadoMultiplicacion}")
print("----------------------------")
print(f"Resultado División: {resultadoDivision}")
print("----------------------------")
print(f"Resultado Módulo: {resultadoModulo}")
print("----------------------------")
print(f"Resultado Adición: {resultadoAdicion}")
print("----------------------------")
print(f"Resultado Sustracción: {resultadoSustraccion}")
print("----------------------------")